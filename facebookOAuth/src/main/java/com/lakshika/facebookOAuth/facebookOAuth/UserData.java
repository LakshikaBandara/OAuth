package com.lakshika.facebookOAuth.facebookOAuth;

public class UserData {

	private String Name;
	private String Link;
	
	
	public UserData() {
		
	}
	public String getName() {
		return Name;
	}
	public void setName(String name) {
		Name = name;
	}
	public String getLink() {
		return Link;
	}
	public void setLink(String link) {
		Link = link;
	}
	
	
}
